# --- Core Presentation Logic ---

def create_presentation_from_template(template_file, title, content_bullets):
    """
    Creates a new presentation based on a template, adding a title and content slide.
    Provides robust error handling for missing slide layouts.
    """
    try:
        prs = Presentation(template_file)
    except Exception as e:
        st.error(f"❌ Error reading the template file. Please ensure it's a valid .pptx file. Details: {e}")
        return None

    # --- Add Title Slide (Layout 0) ---
    if len(prs.slide_layouts) > 0:
        title_slide_layout = prs.slide_layouts[0]
        slide = prs.slides.add_slide(title_slide_layout)
        
        # Set title and subtitle, with checks for placeholder existence
        if slide.shapes.title:
            slide.shapes.title.text = title
        if slide.placeholders and len(slide.placeholders) > 1:
            slide.placeholders[1].text = "Generated by AI Presentation Architect"
    else:
        st.warning("⚠️ Warning: Your template does not have any slide layouts. Cannot add a title slide.")

    # --- Add Content Slide (Layout 1: Title and Content) ---
    if len(prs.slide_layouts) > 1:
        content_slide_layout = prs.slide_layouts[1]
        slide = prs.slides.add_slide(content_slide_layout)
        
        if slide.shapes.title:
            slide.shapes.title.text = "Key Discussion Points"
            
        if len(slide.shapes.placeholders) > 1:
            body_shape = slide.shapes.placeholders[1]
            tf = body_shape.text_frame
            tf.clear()  # Clear placeholder content

            for bullet in content_bullets:
                p = tf.add_paragraph()
                p.text = bullet
                p.level = 0
            tf.add_paragraph().text = "" # Add some space at the end
        else:
            st.warning("⚠️ Warning: Content slide layout is missing a body placeholder.")
    else:
        st.error("❌ Error: Template must have at least two layouts (Title, Title and Content) to function correctly.")
        return None

    # Save to an in-memory buffer
    bio = io.BytesIO()
    prs.save(bio)
    bio.seek(0)
    return bio

# --- Groq LLM Content Generation ---

def generate_content_with_groq(api_key, model, prompt):
    """
    Generates presentation content using the Groq API with a selected model.
    """
    if not api_key:
        st.error("Groq API key is not configured. Deploy on Streamlit Community Cloud and set it in your secrets.")
        return ""
        
    client = Groq(api_key=api_key)
    try:
        chat_completion = client.chat.completions.create(
            messages=[
                {
                    "role": "system",
                    "content": "You are a world-class assistant for generating content for business presentations. Your response MUST be a list of concise, impactful bullet points. Do not include a title, introduction, or any summary. Each bullet point must start on a new line."
                },
                {
                    "role": "user",
                    "content": prompt,
                }
            ],
            model=model,
            temperature=0.7,
            max_tokens=1024,
        )
        return chat_completion.choices[0].message.content
    except Exception as e:
        st.error(f"An error occurred with the Groq API: {e}")
        return ""


